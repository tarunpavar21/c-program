//C Program to Add Two Integer
// here we define 3 variable two variable for define value and third for storing the answer of addition 
// and simple we just adding two number 

#include <stdio.h>

int main() {
    int a, b, sum = 0;

    // Read two numbers from the user
    printf("Enter integer A: ");
    scanf("%d", &a);  
    printf("Enter integer B: ");
    scanf("%d", &b);  
    // Calculate the addition of a and b using the '+' operator
    sum = a + b;

    // Print the result
    printf("Sum: %d", sum);

    return 0;
}



