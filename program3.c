//Reading Integer values in C
// C program to take an integer
// as input and print it


#include <stdio.h>

int main()
{
	// Declare the variables
	int number;

	// Input the integer
	printf("Enter the integer: ");
	scanf("%d", &number);

	// Display the integer
	printf("Entered integer is: %d", number);

	return 0;
}
